class Themes():

	def set_theme(theme):
		
		if theme == 'Default':
			background = 'Default'
			color_scheme = 'Default'
			font = 'Default'

		elif theme == 'Cheese Cube':
			background = 'Yellow'
			color_scheme = 'Cheese Cube'
			font = 'Cheese Cube'
		
		elif theme == 'Christmas Warehouse':
			background = 'Present'
			color_scheme = 'Green/Red'
			font = 'Christmas Warehouse'

		elif theme == 'Steam':
			background = 'Blue/Grey'
			color_scheme = 'Black'
			font = 'Steam'